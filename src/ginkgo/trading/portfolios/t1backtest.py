    # [订单持久化] FILLED/PARTIAL_FILLED 状态持久化位置
    # 事件由 TradeGateway.on_order_partially_filled() 路由到此处
    def on_order_partially_filled(self, event) -> None:
        try:
            if self.is_event_from_future(event):
                return
            order = getattr(event, "order", None)
            if order is None:
                self.blog.log_engine_error_event(error_code="PARTIAL_FILL_NO_ORDER", error_message="Missing order payload")
                return

            # 先从事件中获取成交信息
            qty = int(getattr(event, "filled_quantity", 0) or 0)
            price = to_decimal(getattr(event, "fill_price", 0) or 0)
            fee = to_decimal(getattr(event, "commission", 0) or 0)
            if qty <= 0 or price <= 0:
                return

            # 保存订单记录（FILLED 状态）
            self._save_order_record(order, ORDERSTATUS_TYPES.FILLED, price, qty, fee)

            # 🔥 [CRITICAL FIX] 只有在验证通过后才添加到成交订单列表
            # 🎯 订单统计更新：跟踪已成交的订单
            if order not in self._orders:
                self._orders.append(order)

            # 添加到成交订单列表 (只有真实成交的订单)
            self._filled_orders.append(order)

            direction = getattr(order, "direction", None) or getattr(event, "direction", None)
            code = event.code

            # 添加订单部分成交的关键事件流日志
            GLOG.INFO(
                f"[FILL] {direction.name} {code} {qty}shares @ {price} Fee:{fee} TotalFilled:{order.transaction_volume}/{order.volume} Order:{order.uuid[:8]} Portfolio:{self.uuid[:8]}"
            )

            # 记录订单成交事件到ClickHouse
            self.blog.fill(
                order_id=order.uuid,
                price=price,
                volume=qty,
                commission=fee,
                symbol=code,
                direction=direction.value if hasattr(direction, 'value') else str(direction),
                msg=f"成交: {direction.name} {code} {qty}股@{price} 手续费{fee:.2f}",
            )

            fill_cost = price * qty + fee

            # 累计成交 + 扣减剩余冻结资金（ADR-010 V5：settle，内部守 min 截断 + remain 兜底 + ≥0）
            order.settle(qty, price, fee)

            is_final = (
                getattr(event, "order_status", None) == ORDERSTATUS_TYPES.FILLED
                or order.transaction_volume >= order.volume
            )

            # LONG 部分成交：从冻结资金扣除成交成本，根据是否最终成交决定是否释放剩余资金
            if direction == DIRECTION_TYPES.LONG:
                # 如果不是最终成交，不解冻剩余资金；如果是最终成交，解冻所有剩余资金
                unfreeze_remain = order.remain if is_final else Decimal("0")

                # 从冻结资金中扣除成交成本（settle 已扣 order.remain，此处仅 portfolio 侧同步）
                self.deduct_from_frozen(cost=fill_cost, unfreeze_remain=unfreeze_remain)
                if is_final:
                    order.release_frozen()

                self.add_fee(fee)

                pos = self.get_position(code)
                if pos is None:
                    p = Position(
                        portfolio_id=self.uuid,
                        engine_id=self.engine_id,
                        task_id=self.task_id,
                        code=code,
                        cost=price,
                        volume=qty,
                        price=price,
                        frozen=0,
                        fee=fee,
                        uuid=uuid.uuid4().hex,
                    )
                    self.add_position(p)
                    # 添加Position创建的关键事件流日志
                    GLOG.INFO(
                        f"[POSITION] LONG {code} {qty}shares @ {price} Fee:{fee} Portfolio:{self.uuid[:8]} Position:{p.uuid[:8]}"
                    )
                else:
                    pos.deal(DIRECTION_TYPES.LONG, price, qty)
                    # 添加Position更新的关键事件流日志
                    GLOG.INFO(
                        f"[POSITION] LONG {code} +{qty}shares @ {price} Total:{pos.volume} Portfolio:{self.uuid[:8]} Position:{pos.uuid[:8]}"
                    )


            # SHORT 部分成交：入账现金（成交额-手续费），已冻结仓位随成交递减，剩余冻结保持
            elif direction == DIRECTION_TYPES.SHORT:
                proceeds = price * qty - fee
                self.add_cash(proceeds)
                self.add_fee(fee)

                pos = self.get_position(code)
                if pos is None:
                    self.blog.log_engine_error_event(error_code="NO_POSITION_FOR_SHORT", error_message=f"No position for {code}")
                else:
                    pos.deal(DIRECTION_TYPES.SHORT, price, qty)

            else:
                self.blog.log_engine_error_event(error_code="UNKNOWN_DIRECTION", error_message=f"Unknown direction for {code}")

            # 更新组合指标
            self.update_worth()
            self.update_profit()

            # 分析器 hooks：在 fill 处理完成后触发，确保 portfolio_info 包含最新状态
            for func in self._analyzer_activate_hook[RECORDSTAGE_TYPES.ORDERPARTIALLYFILLED]:
                func(RECORDSTAGE_TYPES.ORDERPARTIALLYFILLED, self.get_info())
            for func in self._analyzer_record_hook[RECORDSTAGE_TYPES.ORDERPARTIALLYFILLED]:
                func(RECORDSTAGE_TYPES.ORDERPARTIALLYFILLED, self.get_info())

        except Exception as e:
            self.blog.log_engine_error_event(error_code="PARTIAL_FILL_FAILED", error_message=str(e))

